export const getRandomWordPair = (difficulty) => {
    let list;
    if (difficulty === 'mix') {
        // Combine easy and medium logic
        list = [...wordLists.easy, ...wordLists.medium];
    } else {
        list = wordLists[difficulty] || wordLists.medium;
    }

    // Safety check just in case
    if (!list || list.length === 0) list = wordLists.medium;

    return list[Math.floor(Math.random() * list.length)];
};
